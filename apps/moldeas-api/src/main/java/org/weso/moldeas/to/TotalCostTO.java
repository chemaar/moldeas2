package org.weso.moldeas.to;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;

@XmlRootElement(name = "totalCostTO")
@XmlAccessorType(XmlAccessType.FIELD)
@XmlType(name = "totalCostTO", propOrder = {
    "min",
    "max"
})
public class TotalCostTO {

	private double min;
	private double max;
	public double getMin() {
		return min;
	}
	public void setMin(double min) {
		this.min = min;
	}
	public double getMax() {
		return max;
	}
	public void setMax(double max) {
		this.max = max;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		long temp;
		temp = Double.doubleToLongBits(max);
		result = prime * result + (int) (temp ^ (temp >>> 32));
		temp = Double.doubleToLongBits(min);
		result = prime * result + (int) (temp ^ (temp >>> 32));
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		TotalCostTO other = (TotalCostTO) obj;
		if (Double.doubleToLongBits(max) != Double.doubleToLongBits(other.max))
			return false;
		if (Double.doubleToLongBits(min) != Double.doubleToLongBits(other.min))
			return false;
		return true;
	}
	@Override
	public String toString() {
		return "TotalCostTO [min=" + min + ", max=" + max + "]";
	}
	public TotalCostTO(double min, double max) {
		super();
		this.min = min;
		this.max = max;
	}
	public TotalCostTO() {
		super();
		// TODO Auto-generated constructor stub
	}
	
	
}
